
package br.com.programaestagio;

public class Pessoa {
    private String nome;
    private String logradouro;
    private String bairro;
    private String municipio;
    private String uf;
    private double cep;
    private String telefone;
    private String email;
    
        
    // Getters and setters
 public String getNome() { return nome; }
 public void setNome(String nome) {this.nome = nome; }
 
 public String getLogradouro() { return logradouro; }
 public void setLogradouro(String logradouro) {this.logradouro = logradouro; }
 
 public String getBairro() { return bairro; }
 public void setBairro(String bairro) {this.bairro = bairro; }
 
 public String getMunicípio () { return municipio; }
 public void setMunicípio(String município) {this.municipio = municipio; }
 
 public String getUF() { return uf; }
 public void setUF(String logradouro) {this.uf = uf; }
 
 public Double getCEP() { return cep; }
 public void setCEP(Double cep) {this.cep = cep; }
 
 public String getTelefone() { return telefone; }
 public void setTelefone(String telefone) {this.telefone = telefone; }
 
 public String getEmail() { return email; }
 public void setEmail(String email) {this.email = email; }
  
}

